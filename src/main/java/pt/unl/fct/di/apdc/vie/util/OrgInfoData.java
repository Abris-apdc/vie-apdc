package pt.unl.fct.di.apdc.vie.util;

public class OrgInfoData {

	
	private String name, address, service, role, email;

    public OrgInfoData() {
    	
    }
    
    public OrgInfoData(String name, String address, String service, String role, String email) {
        this.name = name;
        this.address = address;
        this.service = service;
        this.role = role;
        this.email = email;
    }
    

}
